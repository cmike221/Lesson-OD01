# 1. **Удалить пробелы и привести к одному регистру**:
#    - Удалить все пробелы из строки.
#    - Привести все символы к нижнему регистру для игнорирования регистра.
#
# 2. **Инвертировать строку**:
#    - Создать новую строку, которая будет являться обратной к исходной.
#
# 3. **Сравнить строки**:
#    - Сравнить исходную строку (после обработки) с инвертированной строкой.
#    - Если они одинаковые, значит, строка является палиндромом.
#    - Если они разные, значит, строка не является палиндромом.

def is_palindrome(s):
    # Шаг 1: Удаляем пробелы и приводим к нижнему регистру
    cleaned_s = ''.join(s.split()).lower()

    # Шаг 2: Инвертируем строку
    reversed_s = cleaned_s[::-1]

    # Шаг 3: Сравниваем строки
    return cleaned_s == reversed_s


# Примеры использования
print(is_palindrome("A man a plan a canal Panama"))  # True
print(is_palindrome("Hello"))  # False
print(is_palindrome("Racecar"))  # True
